sub NEW
{
    shift->SUPER::NEW(@_[0..2]);

    $image0 = Qt::Pixmap($image0_data);
    $image1 = Qt::Pixmap($image1_data);
    $image2 = Qt::Pixmap($image2_data);
    $image3 = Qt::Pixmap($image3_data);
    $image4 = Qt::Pixmap($image4_data);
