my $file;
my $local;
my $is_modified;
my $VERSION='0.1';

sub init
{
    setCaption(trUtf8("Locales editor for slack-get - version $VERSION") );
    $local = slackget10::Local->new();
}


sub destroy
{
    if($is_modified)
    {
	my $resp = Qt::MessageBox::warning ( this, 'Save modifications', "Do you want to save the last changes ?",&Qt::MessageBox::Yes, &Qt::MessageBox::No, &Qt::MessageBox::NoButton );
	if($resp == &Qt::MessageBox::Yes) 
	{
	    saveButton_clicked();
	}
    }
}


sub openButton_clicked
{
    $file = Qt::FileDialog::getOpenFileName("$ENV{PWD}","XML files (*.xml)",this,"open file dialog", "Choose a \"Local\" file to open" );
    loadLocalFile($file);
}


sub removeMessageButton_clicked
{
    my $item = lv_messages->selectedItem();
    lv_messages->takeItem($item) if(defined($item) && $item);
    $is_modified=1;
}


sub addMessageButton_clicked
{
     my $name = Qt::InputDialog::getText("slack-get Locales editor", "Please give an index name for the new message :");
    my $value = Qt::InputDialog::getText("slack-get Locales editor", "You can now type your message (you can use the Qt rich text format which support basic HTML like &lt;br&gt;, &lt;u&gt;, &lt;b&gt; tags) :");
     if($name !~ /^$/ && $value !~ /^$/)
     {
	createItem(lv_messages,$name,$value);
	$is_modified=1;
     }
}


sub loadLocalFile # SLOT: ( const QString & )
{
    my $filename = shift;
    if(defined($filename) && $filename)
    {
	if( -e $filename )
	{
	    if($local->Load($filename))
	    {
		&fillListViewFromLocalObject;
	    }
	    else
	    {
		Qt::MessageBox::critical(this,'Error',"An error occured while loading local from \"$filename\".<br>Maybe the XML file is not well formed.",&Qt::MessageBox::Ok, &Qt::MessageBox::NoButton, &Qt::MessageBox::NoButton);
	    }
	}
	else
	{
	    Qt::MessageBox::critical(this,'File not found',"the file : \"$filename\" was not found.<br>System say :<br>$!",&Qt::MessageBox::Ok, &Qt::MessageBox::NoButton, &Qt::MessageBox::NoButton);
	}
    }
}


sub fillListViewFromLocalObject
{
    lv_messages->clear();
    foreach ( $local->get_indexes )
    {
        createItem(lv_messages, $_, $local->Get($_));
    }
}


sub createItem # SLOT: ( QListView *, const QString &, const QString & )
{
    my ($lv,$idx,$txt) = @_ ;
    my $item = Qt::ListViewItem( $lv );
    $item->setText(0,$idx);
    $item->setText(1,$txt);
}


sub saveButton_clicked
{
     $file = Qt::FileDialog::getSaveFileName("$file","XML files (*.xml)",this,"save file dialog","Choose a filename to save the \"Local\" under" );
     my $ok=0;
     my $name = Qt::InputDialog::getText("Locales editor", "Enter a name for this langpack (users will see this name) :", &Qt::LineEdit::Normal,$local->name, $ok, this );
    $local->name($name) if($ok);
     our $xml = "<?xml version=\"1.0\" encoding=\"utf-8\" standalone=\"yes\"?>\n<local name=\"".$local->name."\">\n";
     my $item = lv_messages->firstChild();
    $xml .= "\t<message id=\"".$item->text(0)."\"><![CDATA[".$item->text(1)."]]></message>\n";
     while($item = $item->nextSibling())
     {
          $xml .= "\t<message id=\"".$item->text(0)."\"><![CDATA[".$item->text(1)."]]></message>\n";
     }
     $xml .= "\n</local>\n";
     my $file = slackget10::File->new($file, 'no-auto-load'=>1);
     $file->Add($xml);
     $file->Write_and_close  ;
     $is_modified = 0;
     emit(localSaved);
}


sub lv_messages_doubleClicked # SLOT: ( QListViewItem * )
{
    my $item = shift;
    my $ok=0;
    my $value = Qt::InputDialog::getText("slack-get Locales editor", "You can now type your message for the message indexed as <font color='blue'><b>\"".$item->text(0)."\"</b></font> <br>(you can use the Qt rich text format which support basic HTML like &lt;br&gt;, &lt;u&gt;, &lt;b&gt; tags) :", &Qt::LineEdit::Normal,$item->text(1),$ok,this);
    $item->setText(1,$value) if($ok) ;
    $is_modified=1;
}


